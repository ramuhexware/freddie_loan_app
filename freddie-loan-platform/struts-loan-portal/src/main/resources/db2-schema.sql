-- ============================================================

CREATE SCHEMA FREDDIE_LOANS;

-- ============================================================
-- CUSTOMERS table
-- ============================================================
CREATE TABLE FREDDIE_LOANS.CUSTOMERS (
    CUSTOMER_ID     VARCHAR(36)     NOT NULL,
    FIRST_NAME      VARCHAR(100)    NOT NULL,
    LAST_NAME       VARCHAR(100)    NOT NULL,
    EMAIL           VARCHAR(200)    NOT NULL,
    PHONE_NUMBER    VARCHAR(20),
    SSN             VARCHAR(11),
    DATE_OF_BIRTH   TIMESTAMP,
    CUSTOMER_STATUS VARCHAR(20)     NOT NULL DEFAULT 'ACTIVE',
    ADDRESS_LINE1   VARCHAR(200),
    ADDRESS_LINE2   VARCHAR(200),
    CITY            VARCHAR(100),
    STATE           VARCHAR(2),
    ZIP_CODE        VARCHAR(10),
    CREDIT_SCORE    INTEGER,
    CREATED_AT      TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT      TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT PK_CUSTOMERS PRIMARY KEY (CUSTOMER_ID),
    CONSTRAINT CHK_CUST_STATUS CHECK (CUSTOMER_STATUS IN ('ACTIVE','INACTIVE','SUSPENDED'))
);

CREATE INDEX IX_CUST_EMAIL   ON FREDDIE_LOANS.CUSTOMERS (EMAIL);
CREATE INDEX IX_CUST_STATUS  ON FREDDIE_LOANS.CUSTOMERS (CUSTOMER_STATUS);
CREATE INDEX IX_CUST_NAME    ON FREDDIE_LOANS.CUSTOMERS (LAST_NAME, FIRST_NAME);

-- ============================================================
-- LOAN_APPLICATIONS table
-- ============================================================
CREATE TABLE FREDDIE_LOANS.LOAN_APPLICATIONS (
    LOAN_ID             VARCHAR(36)     NOT NULL,
    CUSTOMER_ID         VARCHAR(36)     NOT NULL,
    LOAN_TYPE           VARCHAR(50)     NOT NULL,
    LOAN_AMOUNT         DECIMAL(18,2)   NOT NULL,
    PROPERTY_VALUE      DECIMAL(18,2),
    PROPERTY_ADDRESS    VARCHAR(500),
    INTEREST_RATE       DECIMAL(6,4),
    LOAN_TERM_MONTHS    INTEGER,
    LOAN_STATUS         VARCHAR(30)     NOT NULL DEFAULT 'PENDING',
    APPLICATION_DATE    TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    DECISION_DATE       TIMESTAMP,
    DISBURSEMENT_DATE   TIMESTAMP,
    APPROVED_AMOUNT     DECIMAL(18,2),
    REJECTION_REASON    VARCHAR(1000),
    CREATED_BY          VARCHAR(100),
    UPDATED_AT          TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    VERSION             BIGINT          DEFAULT 0,
    CONSTRAINT PK_LOAN_APPS PRIMARY KEY (LOAN_ID),
    CONSTRAINT FK_LOAN_CUSTOMER FOREIGN KEY (CUSTOMER_ID)
        REFERENCES FREDDIE_LOANS.CUSTOMERS (CUSTOMER_ID),
    CONSTRAINT CHK_LOAN_TYPE CHECK (LOAN_TYPE IN ('PURCHASE','REFINANCE','HELOC','HOME_EQUITY')),
    CONSTRAINT CHK_LOAN_STATUS CHECK (LOAN_STATUS IN
        ('PENDING','SUBMITTED','UNDER_REVIEW','APPROVED','REJECTED','DISBURSED','CLOSED'))
);

CREATE INDEX IX_LOAN_CUSTOMER ON FREDDIE_LOANS.LOAN_APPLICATIONS (CUSTOMER_ID);
CREATE INDEX IX_LOAN_STATUS   ON FREDDIE_LOANS.LOAN_APPLICATIONS (LOAN_STATUS);
CREATE INDEX IX_LOAN_DATE     ON FREDDIE_LOANS.LOAN_APPLICATIONS (APPLICATION_DATE);
CREATE INDEX IX_LOAN_TYPE     ON FREDDIE_LOANS.LOAN_APPLICATIONS (LOAN_TYPE);

-- ============================================================
-- UNDERWRITING_DECISIONS table
-- ============================================================
CREATE TABLE FREDDIE_LOANS.UNDERWRITING_DECISIONS (
    DECISION_ID         VARCHAR(36)     NOT NULL,
    LOAN_ID             VARCHAR(36)     NOT NULL,
    UNDERWRITER_ID      VARCHAR(100),
    UNDERWRITER_NAME    VARCHAR(200),
    DECISION            VARCHAR(20)     NOT NULL,
    APPROVED_AMOUNT     DECIMAL(18,2),
    RECOMMENDED_RATE    DECIMAL(6,4),
    CONDITIONS          VARCHAR(2000),
    REJECTION_REASON    VARCHAR(2000),
    DEBT_TO_INCOME_RATIO INTEGER,
    LOAN_TO_VALUE_RATIO  INTEGER,
    CREDIT_SCORE_USED    INTEGER,
    RISK_CATEGORY       VARCHAR(10),
    NOTES               VARCHAR(4000),
    DECISION_DATE       TIMESTAMP,
    CREATED_AT          TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT PK_UW_DECISIONS PRIMARY KEY (DECISION_ID),
    CONSTRAINT FK_UW_LOAN FOREIGN KEY (LOAN_ID)
        REFERENCES FREDDIE_LOANS.LOAN_APPLICATIONS (LOAN_ID),
    CONSTRAINT CHK_UW_DECISION CHECK (DECISION IN ('APPROVED','REJECTED','CONDITIONAL')),
    CONSTRAINT CHK_RISK_CAT CHECK (RISK_CATEGORY IN ('LOW','MEDIUM','HIGH'))
);

CREATE INDEX IX_UW_LOAN ON FREDDIE_LOANS.UNDERWRITING_DECISIONS (LOAN_ID);

-- ============================================================
-- AUDIT_LOG table (written by AuditInterceptor on EVERY action)
-- ============================================================
CREATE TABLE FREDDIE_LOANS.AUDIT_LOG (
    AUDIT_ID            BIGINT          NOT NULL GENERATED ALWAYS AS IDENTITY
                                        (START WITH 1 INCREMENT BY 1),
    ACTION_NAMESPACE    VARCHAR(100),
    ACTION_NAME         VARCHAR(100),
    ACTION_METHOD       VARCHAR(100),
    SESSION_USER_ID     VARCHAR(100),
    REMOTE_IP           VARCHAR(45),
    REQUEST_PARAMS      VARCHAR(2000),
    RESULT_CODE         VARCHAR(50),
    EXEC_TIME_MS        BIGINT,
    ERROR_MESSAGE       VARCHAR(500),
    AUDIT_TIMESTAMP     TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT PK_AUDIT_LOG PRIMARY KEY (AUDIT_ID)
);

CREATE INDEX IX_AUDIT_TS   ON FREDDIE_LOANS.AUDIT_LOG (AUDIT_TIMESTAMP DESC);
CREATE INDEX IX_AUDIT_USER ON FREDDIE_LOANS.AUDIT_LOG (SESSION_USER_ID);
CREATE INDEX IX_AUDIT_ACT  ON FREDDIE_LOANS.AUDIT_LOG (ACTION_NAME);

