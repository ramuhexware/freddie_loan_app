-- ============================================================
-- LOAN_DOCUMENTS table
-- Stores file metadata for uploaded documents.
-- Actual file bytes live on the filesystem (managed by FileService).
--
-- Columns map directly to the LoanDocument model and DocumentDao.
-- FileOperationsService writes a parallel .meta.properties sidecar
-- file (via FileOutputStream) next to each stored file for recovery.
-- ============================================================
CREATE TABLE FREDDIE_LOANS.LOAN_DOCUMENTS (
    DOCUMENT_ID         VARCHAR(36)     NOT NULL,
    LOAN_ID             VARCHAR(36)     NOT NULL,
    CUSTOMER_ID         VARCHAR(36),
    DOCUMENT_TYPE       VARCHAR(50)     NOT NULL,
    ORIGINAL_FILE_NAME  VARCHAR(500)    NOT NULL,
    STORED_FILE_NAME    VARCHAR(500)    NOT NULL,
    FILE_PATH           VARCHAR(2000)   NOT NULL,
    FILE_SIZE_BYTES     BIGINT          NOT NULL DEFAULT 0,
    MIME_TYPE           VARCHAR(200),
    UPLOADED_BY         VARCHAR(100),
    STATUS              VARCHAR(20)     NOT NULL DEFAULT 'PENDING_REVIEW',
    REVIEW_NOTES        VARCHAR(1000),
    UPLOADED_AT         TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    REVIEWED_AT         TIMESTAMP,
    CONSTRAINT PK_LOAN_DOCS    PRIMARY KEY (DOCUMENT_ID),
    CONSTRAINT FK_DOC_LOAN     FOREIGN KEY (LOAN_ID)
        REFERENCES FREDDIE_LOANS.LOAN_APPLICATIONS (LOAN_ID)
        ON DELETE CASCADE,
    CONSTRAINT FK_DOC_CUSTOMER FOREIGN KEY (CUSTOMER_ID)
        REFERENCES FREDDIE_LOANS.CUSTOMERS (CUSTOMER_ID),
    CONSTRAINT CHK_DOC_TYPE    CHECK (DOCUMENT_TYPE IN (
        'PAY_STUB','TAX_RETURN','BANK_STATEMENT','APPRAISAL',
        'PURCHASE_AGREEMENT','GOVERNMENT_ID','INSURANCE_POLICY','OTHER')),
    CONSTRAINT CHK_DOC_STATUS  CHECK (STATUS IN ('PENDING_REVIEW','APPROVED','REJECTED'))
);

-- Indexes for common query patterns used by DocumentDao
CREATE INDEX IX_DOC_LOAN_ID   ON FREDDIE_LOANS.LOAN_DOCUMENTS (LOAN_ID);
CREATE INDEX IX_DOC_CUSTOMER  ON FREDDIE_LOANS.LOAN_DOCUMENTS (CUSTOMER_ID);
CREATE INDEX IX_DOC_STATUS    ON FREDDIE_LOANS.LOAN_DOCUMENTS (STATUS);
CREATE INDEX IX_DOC_TYPE      ON FREDDIE_LOANS.LOAN_DOCUMENTS (DOCUMENT_TYPE);
CREATE INDEX IX_DOC_UPLOADED  ON FREDDIE_LOANS.LOAN_DOCUMENTS (UPLOADED_AT DESC);

